#include <string.h>
#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != (char)c)
	{
		if (!str[i])
			return (NULL);
		i++;
	}
	return ((char*)&str[i]);
}
